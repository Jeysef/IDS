-- customers count by city
SELECT COUNT(*) as USERS_COUNT, A.CITY as CITY
FROM REGISTERED_CUSTOMERS C
         JOIN ADDRESSES A on C.ADDRESS_ID = A.ID
GROUP BY A.CITY;

-- no of items sold per city
SELECT COUNT(*) as PROCUCTS_COUNT, C.NAME as CATEGORY
FROM CATEGORIES C
         JOIN PRODUCT_CATEGORIES PC on C.ID = PC.CATEGORY_ID
         JOIN PRODUCTS P on PC.PRODUCT_ID = P.ID
GROUP BY C.NAME;

