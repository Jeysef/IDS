-- Nejprodávanější produkty
CREATE VIEW BEST_SELLING AS
SELECT P.NAME as PRODUCT_NAME, C.NAME AS CATEGORY_NAME, OI.PRICE * OI.QUANTITY AS TOTAL_REVENUE
FROM PRODUCTS P
         JOIN PRODUCT_CATEGORIES PC on P.ID = PC.PRODUCT_ID
         JOIN CATEGORIES C on PC.CATEGORY_ID = C.ID
         JOIN ORDER_ITEMS OI ON OI.PRODUCT_ID = P.ID
-- GROUP BY P.NAME, C.NAME, OI.PRICE, OI.QUANTITY
ORDER BY TOTAL_REVENUE DESC;

-- Produkty v kategorii myši seřazené jako nejprodávanější
SELECT *
FROM BEST_SELLING BS
WHERE BS.CATEGORY_NAME = 'Myši'
ORDER BY TOTAL_REVENUE DESC;
