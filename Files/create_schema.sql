-- Počet zákazníků podle města
SELECT COUNT(*) as USERS_COUNT, A.CITY as CITY
FROM REGISTERED_CUSTOMERS C
         JOIN ADDRESSES A on C.ADDRESS_ID = A.ID
GROUP BY A.CITY
ORDER BY USERS_COUNT DESC;

-- Počet prodaných kusů podle kategorie
WITH PRODUCT_SOLD_QUANTITY AS (SELECT NAME, PRODUCTS.ID AS PRODUCT_ID, OI.QUANTITY
                               FROM PRODUCTS
                                        JOIN ORDER_ITEMS OI on PRODUCTS.ID = OI.PRODUCT_ID)

SELECT SUM(P.QUANTITY) as PRODUCTS_COUNT, C.NAME as CATEGORY
FROM PRODUCT_SOLD_QUANTITY P
         JOIN PRODUCT_CATEGORIES PC on P.PRODUCT_ID = PC.PRODUCT_ID
         JOIN CATEGORIES C on PC.CATEGORY_ID = C.ID
GROUP BY C.NAME
ORDER BY PRODUCTS_COUNT DESC;

-- Počet prodaných kusů podle města
SELECT SUM(OI.QUANTITY) AS PRODUCTS_COUNT,
       A.CITY
FROM ORDER_ITEMS OI
         JOIN ORDERS O ON OI.ORDER_ID = O.ID
         JOIN REGISTERED_CUSTOMERS RC ON O.REGISTERED_CUSTOMER_ID = RC.CUSTOMER_ID
         JOIN ADDRESSES A ON RC.ADDRESS_ID = A.ID
